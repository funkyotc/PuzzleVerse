    private val ALL_PUZZLES: List<PregeneratedShikaku> by lazy {
        STATIC_PUZZLES.map { compact ->
            val clues = compact.cluesStr.split(";").map { parts ->
                val clueParts = parts.split(",")
                PregeneratedShikakuClue(
                    row = clueParts[0].toInt(),
                    col = clueParts[1].toInt(),
                    value = clueParts[2].toInt()
                )
            }

            val grid = compact.gridStr.split(";").map { rowStr ->
                rowStr.split(",").map { it.toInt() }
            }

            PregeneratedShikaku(
                id = compact.id,
                difficulty = compact.difficulty,
                clues = clues,
                grid = grid,
                seed = compact.seed
            )
        }
    }

    val PUZZLES_BY_DIFFICULTY: Map<String, List<PregeneratedShikaku>> by lazy {
        ALL_PUZZLES.groupBy { it.difficulty.lowercase().replaceFirstChar { c -> c.uppercaseChar() } }
